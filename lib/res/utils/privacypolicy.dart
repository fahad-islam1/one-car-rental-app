“Allow One Car Rental to access this device’s location?”. It is strongly recommended that you grant this permission as it will allow the app to automatically set your pickup location as your current location.

“Allow One Car Rental to make and manage phone calls?”. It is strongly recommended that you grant this permission as it will allow you to call the driver if needed.

“Allow One Car Rental to access your contacts?”. This permission is only required if you choose to use certain features such as when you refer a friend to One Car Rental, when you add someone to your emergency contacts, or share your ETA or ride status.


